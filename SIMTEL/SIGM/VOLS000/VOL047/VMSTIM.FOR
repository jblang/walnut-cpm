	SUBROUTINE ITIME (H, M, S)

	IMPLICIT INTEGER*2 (A-Z)
	INTEGER*2 TIME (7)

	CALL SYS$NUMTIM (TIME, %VAL(0))
	H = TIME(4)
	M = TIME(5)
	S = TIME(6)
	RETURN

	END
