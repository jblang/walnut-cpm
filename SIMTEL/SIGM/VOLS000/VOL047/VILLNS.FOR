800	CALL NEWSTA(PRSO,0,0,0,-THIEF)		!THIEF TAKES GIFT.
	IF(OTVAL(PRSO).GT.0) GO TO 900		!A TREASURE?
	CALL RSPSUB(512,ODESC2(PRSO))
	RETURN
