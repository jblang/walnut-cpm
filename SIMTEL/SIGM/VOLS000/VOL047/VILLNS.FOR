	OCAPAC(THIEF)=-OCAPAC(THIEF)
	THFACT=.TRUE.
	OFLAG1(STILL)=OFLAG1(STILL).OR.VISIBT
	ODESC1(THIEF)=503
	CALL RSPEAK(510)
