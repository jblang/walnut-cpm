	MVI	A,0CDH
	STA	0	;SET CONTROL-C TRAP
	LXI	D,BBSMSG
	CALL	SEND
	JMP	FIN
;
;SUBROUTINES
MOVER	MOV	A,M
	CPI	0
	RZ
	STAX	D
	INX	D
	INX	H
	JMP	MOVER
GETSTR	PUSH	D
	CALL	GETC
	POP	D
	CPI	CR
	JZ	WRBUFF
	CPI	'U'-40H
	JZ	REASK
	CPI	'H'-40H
	JZ	REASK
	CPI	7FH
	JZ	REASK
	CPI	5FH
	JZ	REASK
	STAX	D
	INX	D
	JMP	GETSTR
REASK	CALL	CRLF
	PUSH	D
	LXI	D,RTYMSG
	CALL	SEND
	POP	D
	CALL	CRLF
	PUSH	D
	MVI	E,'?'
	MVI	C,2
	CALL	BDOS
	MVI	E,07
	MVI	C,2
	CALL	BDOS
	POP	D
	LXI	H,DBUFF
REASK1	MOV	A,M
	CPI	':'
	JZ	REASKED
	INX	H
	JMP	REASK1
REASKED	INX	H	;ONE MORE TIME FOR SPACE
	INX	H	;PUTS US BACK WHERE WE START IN DMA.
	XCHG
	JMP	GETSTR
CRLF	PUSH	D
	PUSH	H
	MVI	E,CR
	MVI	C,2
	CALL	BDOS
	MVI	E,LF
	MVI	C,2
	CALL	BDOS
	MVI	E,BELL
	MVI	C,2
	CALL	BDOS
	POP	H
	POP	D
	RET
PRINTH	LXI	D,DBUFF
	MOV	A,M
PRINTH1	STAX	D
	INX	H
	INX	D
	MOV	A,M
	CPI	0
	JNZ	PRINTH1
PRINTHE	XCHG
	RET
GETC	MVI	C,CONSTAT
	CALL	BDOS
	ORA	A
	JZ	GETC
	MVI	C,READCON
	CALL	BDOS
	RET
SEND	PUSH	H
	MVI	C,PRINT
	CALL	BDOS
	POP	H
	RET
WRBUFF	MVI	A,CR
	STAX	D
	INX	D
	MVI	A,LF
	STAX	D
	INX	D
WRBUFF1	MOV	A,E
	CPI	0FFH
	JZ	WRBUFF2
	XRA	A
	STAX	D
	INX	D
	JMP	WRBUFF1
WRBUFF2	LXI	D,FCB
	MVI	C,WRITE
	CALL	BDOS
	CPI	0
	RZ
	CPI	1
	JZ	EXTERR
	CPI	2
	JZ	DIRFUL
	CPI	255
	JZ	DIRFUL
	JMP	GENERR
EXTERR	LXI	D,EXERMSG
	CALL	SEND
	CALL	FIN
	JMP	0
DIRFUL	LXI	D,DIRERMSG
	CALL	SEND
	CALL	FIN
	JMP	0
GENERR	LXI	D,GERRMSG
	CALL	SEND
	CALL	FIN
	JMP	0
