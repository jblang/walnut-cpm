	LXI	D,Q9
	CALL	SEND
	XCHG
	CALL	GETSTR
	LXI	H,Q9AH
	CALL	PRINTH
	LXI	D,Q9A
	CALL	SEND
	XCHG
	CALL	GETSTR
	LXI	H,Q10H
	CALL	PRINTH
	LXI	D,Q10
	CALL	SEND
	XCHG
	CALL	GETSTR
	LXI	H,Q11H
	CALL	PRINTH
	LXI	D,Q11
	CALL	SEND
	XCHG
	CALL	GETSTR
	LXI	H,Q12H
	CALL	PRINTH
	LXI	D,Q12
	CALL	SEND
	XCHG
	CALL	GETSTR
	LXI	H,Q13H
	CALL	PRINTH
	LXI	D,Q13
	CALL	SEND
	XCHG
	CALL	GETSTR
	LXI	H,Q14H
	CALL	PRINTH
	LXI	D,Q14
	CALL	SEND
	XCHG
	CALL	GETSTR
	LXI	D,FCB
	MVI	C,CLOSE
	CALL	BDOS	;CLOSE APPLICATION FILE
;
;RENAME FILE NOW SO USER CAN'T WRITE OVER THE INFORMATION
;AND IN CASE SOMEONE ELSE WANTS TO APPLY, HE WON'T WRITE
;OVER THIS ONE BEFORE I SEE IT. (DUE TO FACT THAT PGM
;CAN'T FIND FILE ON NEXT INVOCATION OF PROGRAM)
REN	LXI	H,FILENM2	;NEW FILENAME
	LXI	D,FCB2+1
	CALL	MOVER
	LXI	D,FCB
	MVI	C,RENAME
	CALL	BDOS	;RENAME FILE FOR PROTECTION
;
	IF	V2
;SET FILE ATTRIBUTE TO .SYS SO FILE IS INVISIBLE TO CALLERS
	MVI	C,ATTRIB
	CALL	BDOS
	ENDIF
;
;WE ARE DONE WITH THE FILE NOW SO LETS GET OUTTA HERE...
	LXI	D,EQMSG
	CALL	SEND
