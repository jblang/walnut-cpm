	MVI	A,'0'
	CMP	M
	JNZ	IDERR	;NO PASSWORD FOUND
	XRA	A
	MVI	B,8
L1	INX	H
	ADD	M
	DCR	B
	JNZ	L1
	LXI	H,USERCT	;NUMBER OF USER #'S TO CHECK
	MOV	B,M
	LXI	H,USERS
CHK	CMP	M
	JZ	CHOK	;PASSWORD FOUND
	INX	H
	DCR	B
	JNZ	CHK
